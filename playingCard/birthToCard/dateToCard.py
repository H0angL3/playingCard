def nameCard(num, sym):
    num_name = str(num)
    img_number = str(num)
    if num == 11:
        num_name = 'Jack'
        img_number = 'jack'
    if num == 12:
        num_name = 'Queen'
        img_number = 'queen'
    if num == 13:
        num_name = 'King'
        img_number = 'king'
    if num == '1':
        num_name = 'Ace'
        img_number = 'ace'

    if sym == 'D':
        sym_name = 'diamonds'
    if sym == 'C':
        sym_name = 'clubs'
    if sym == 'S':
        sym_name = 'spades'
    if sym == 'H':
        sym_name = 'hearts'

    name_card = '{} of {}'.format(num_name, sym_name)
    img_card = '{}_of_{}'.format(img_number, sym_name)

    return name_card, img_card

